package main

import "fmt"

var n = 10
var m = 12
var count = 0
var field = [][]string {
	{"W", ".", ".", ".", ".", ".", ".", ".", ".", "W", "W", "."},
	{".", "W", "W", "W", ".", ".", ".", ".", ".", "W", "W", "W"},
	{".", ".", ".", ".", "W", "W", ".", ".", ".", "W", "W", "."},
	{".", ".", ".", ".", ".", ".", ".", ".", ".", "W", "W", "."},
	{".", ".", ".", ".", ".", ".", ".", ".", ".", "W", ".", "."},
	{".", ".", "W", ".", ".", ".", ".", ".", ".", "W", ".", "."},
	{".", "W", ".", "W", ".", ".", ".", ".", ".", "W", "W", "."},
	{"W", ".", "W", ".", "W", ".", ".", ".", ".", ".", "W", "."},
	{".", "W", ".", "W", ".", ".", ".", ".", ".", ".", "W", "."},
	{".", ".", "W", ".", ".", ".", ".", ".", ".", ".", "W", "."},
}

func main() {

	var str string
	for i:=0; i<n; i++ {
		for j:= 0; j<m; j++ {
			str = field[i][j]
			if str == "W" {
				dps(i,j)
				count++
			}
		}
	}
	fmt.Print(count)
}

func dps(i int, j int) {
	field[i][j] = "."

	for x:=-1; x <=1; x++ {
		for y:=-1; y <=1; y++ {
			if i+x < n && i+x >= 0 && j+y < m && j+y >= 0 {
				if field[i+x][j+y] == "W" {
					dps(i+x, j+y)
				}
			}
		}
	}
}
